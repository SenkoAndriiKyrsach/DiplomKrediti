// таблиця з заголовком
function makeTable(headers, rows, colWidths) {
  const total = colWidths.reduce((a, b) => a + b, 0);
  const mkCell = (text, isHeader) => new TableCell({
    verticalAlign: VerticalAlign.CENTER,
    shading: isHeader ? { fill: "2E75B6", type: ShadingType.CLEAR, color: "auto" } : undefined,
    children: [new Paragraph({
      alignment: AlignmentType.LEFT,
      spacing: { line: 280 },
      children: [new TextRun({ text, font: FONT, size: 22, bold: isHeader, color: isHeader ? "FFFFFF" : "000000" })],
    })],
  });

  const headerRow = new TableRow({
    tableHeader: true,
    children: headers.map((h) => mkCell(h, true)),
  });

  const dataRows = rows.map((row) =>
    new TableRow({ children: row.map((cell) => mkCell(cell, false)) })
  );

  return new Table({
    width: { size: 100, type: WidthType.PERCENTAGE },
    rows: [headerRow, ...dataRows],
  });
}

