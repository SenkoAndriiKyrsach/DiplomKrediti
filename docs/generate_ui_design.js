const FONT = "Times New Roman";
const SIZE = 24; // 12pt in half-points
const SIZE_HEADING1 = 28; // 14pt
const SIZE_HEADING2 = 26; // 13pt

const INDENT_CM = 1.25; // paragraph first-line indent (cm → twips)
const INDENT_TWIPS = convertInchesToTwip(INDENT_CM / 2.54);

function body(text) {
  return new Paragraph({
    alignment: AlignmentType.JUSTIFIED,
    indent: { firstLine: INDENT_TWIPS },
    spacing: { line: 360, before: 0, after: 0 },
    children: [new TextRun({ text, font: FONT, size: SIZE })],
  });
}

function bodyBold(label, rest) {
  return new Paragraph({
    alignment: AlignmentType.JUSTIFIED,
    indent: { firstLine: INDENT_TWIPS },
    spacing: { line: 360, before: 0, after: 0 },
    children: [
      new TextRun({ text: label, font: FONT, size: SIZE, bold: true }),
      new TextRun({ text: rest, font: FONT, size: SIZE }),
    ],
  });
}

function bullet(text) {
  return new Paragraph({
    alignment: AlignmentType.JUSTIFIED,
    indent: { left: convertInchesToTwip(0.5 / 2.54), hanging: convertInchesToTwip(0.3 / 2.54) },
    spacing: { line: 360, before: 0, after: 0 },
    children: [new TextRun({ text: `– ${text}`, font: FONT, size: SIZE })],
  });
}

function caption(text) {
  return new Paragraph({
    alignment: AlignmentType.CENTER,
    spacing: { line: 360, before: 120, after: 120 },
    children: [new TextRun({ text, font: FONT, size: SIZE, italic: true })],
  });
}

function h1(text) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_1,
    alignment: AlignmentType.CENTER,
    spacing: { before: 240, after: 120, line: 360 },
    children: [
      new TextRun({ text, font: FONT, size: SIZE_HEADING1, bold: true }),
    ],
  });
}

function h2(text) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_2,
    alignment: AlignmentType.LEFT,
    indent: { firstLine: INDENT_TWIPS },
    spacing: { before: 200, after: 80, line: 360 },
    children: [
      new TextRun({ text, font: FONT, size: SIZE_HEADING2, bold: true }),
    ],
  });
}

function h3(text) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_3,
    alignment: AlignmentType.LEFT,
    indent: { firstLine: INDENT_TWIPS },
    spacing: { before: 160, after: 60, line: 360 },
    children: [
      new TextRun({ text, font: FONT, size: SIZE, bold: true }),
    ],
  });
}

function empty() {
  return new Paragraph({ spacing: { line: 360 }, children: [] });
}

// Simple 2-column table helper
function twoColTable(rows, headers) {
  const headerRow = new TableRow({
    tableHeader: true,
    children: headers.map((h) =>
      new TableCell({
        shading: { fill: "2E75B6", type: ShadingType.CLEAR, color: "auto" },
        verticalAlign: VerticalAlign.CENTER,
        children: [
          new Paragraph({
            alignment: AlignmentType.CENTER,
            children: [new TextRun({ text: h, font: FONT, size: SIZE, bold: true, color: "FFFFFF" })],
          }),
        ],
      })
    ),
  });

  const dataRows = rows.map(([col1, col2]) =>
    new TableRow({
      children: [
        new TableCell({
          verticalAlign: VerticalAlign.CENTER,
          children: [
            new Paragraph({
              children: [new TextRun({ text: col1, font: FONT, size: SIZE })],
            }),
          ],
        }),
        new TableCell({
          verticalAlign: VerticalAlign.CENTER,
          children: [
            new Paragraph({
              children: [new TextRun({ text: col2, font: FONT, size: SIZE })],
            }),
          ],
        }),
      ],
    })
  );

  return new Table({
    width: { size: 100, type: WidthType.PERCENTAGE },
    rows: [headerRow, ...dataRows],
  });
}

